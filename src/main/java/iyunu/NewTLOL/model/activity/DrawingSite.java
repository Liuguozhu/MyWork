package iyunu.NewTLOL.model.activity;

public class DrawingSite {

	private int mapId;
	private int x;
	private int y;
	private int isDrawing;

	/**
	 * @return the mapId
	 */
	public int getMapId() {
		return mapId;
	}

	/**
	 * @param mapId
	 *            the mapId to set
	 */
	public void setMapId(int mapId) {
		this.mapId = mapId;
	}

	/**
	 * @return the x
	 */
	public int getX() {
		return x;
	}

	/**
	 * @param x
	 *            the x to set
	 */
	public void setX(int x) {
		this.x = x;
	}

	/**
	 * @return the y
	 */
	public int getY() {
		return y;
	}

	/**
	 * @param y
	 *            the y to set
	 */
	public void setY(int y) {
		this.y = y;
	}

	/**
	 * @return the isDrawing
	 */
	public int getIsDrawing() {
		return isDrawing;
	}

	/**
	 * @param isDrawing
	 *            the isDrawing to set
	 */
	public void setIsDrawing(int isDrawing) {
		this.isDrawing = isDrawing;
	}

}
