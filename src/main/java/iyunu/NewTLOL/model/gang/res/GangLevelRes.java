package iyunu.NewTLOL.model.gang.res;

/**
 * 帮派升级表
 * 
 * @author fenghaiyu
 * 
 */
public class GangLevelRes {
	
	private int level;// 级别
	private int exp;// 升级经验
	private int upNum;// 帮派最大人数

	/**
	 * @return the level
	 */
	public int getLevel() {
		return level;
	}

	/**
	 * @param level
	 *            the level to set
	 */
	public void setLevel(int level) {
		this.level = level;
	}

	/**
	 * @return the exp
	 */
	public int getExp() {
		return exp;
	}

	/**
	 * @param exp
	 *            the exp to set
	 */
	public void setExp(int exp) {
		this.exp = exp;
	}

	/**
	 * @return the upNum
	 */
	/**
	 * @return
	 */
	public int getUpNum() {
		return upNum;
	}

	/**
	 * @param upNum
	 *            the upNum to set
	 */
	public void setUpNum(int upNum) {
		this.upNum = upNum;
	}

}
