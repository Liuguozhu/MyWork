package iyunu.NewTLOL.model.blood;

public class BloodAward {
	private int id;
	private String award;
	private String first;
	private String second;
	private String third;

	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}

	/**
	 * @param id
	 *            the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}

	/**
	 * @return the award
	 */
	public String getAward() {
		return award;
	}

	/**
	 * @param award
	 *            the award to set
	 */
	public void setAward(String award) {
		this.award = award;
	}

	/**
	 * @return the first
	 */
	public String getFirst() {
		return first;
	}

	/**
	 * @param first
	 *            the first to set
	 */
	public void setFirst(String first) {
		this.first = first;
	}

	/**
	 * @return the second
	 */
	public String getSecond() {
		return second;
	}

	/**
	 * @param second
	 *            the second to set
	 */
	public void setSecond(String second) {
		this.second = second;
	}

	/**
	 * @return the third
	 */
	public String getThird() {
		return third;
	}

	/**
	 * @param third
	 *            the third to set
	 */
	public void setThird(String third) {
		this.third = third;
	}

}
