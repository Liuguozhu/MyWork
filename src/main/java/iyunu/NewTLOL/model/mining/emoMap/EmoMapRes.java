package iyunu.NewTLOL.model.mining.emoMap;

public class EmoMapRes {
	private int id;
	private int x;
	private int y;
	private int monsterGroupId;

	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}

	/**
	 * @param id
	 *            the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}

	/**
	 * @return the x
	 */
	public int getX() {
		return x;
	}

	/**
	 * @param x
	 *            the x to set
	 */
	public void setX(int x) {
		this.x = x;
	}

	/**
	 * @return the y
	 */
	public int getY() {
		return y;
	}

	/**
	 * @param y
	 *            the y to set
	 */
	public void setY(int y) {
		this.y = y;
	}

	/**
	 * @return the monsterGroupId
	 */
	public int getMonsterGroupId() {
		return monsterGroupId;
	}

	/**
	 * @param monsterGroupId
	 *            the monsterGroupId to set
	 */
	public void setMonsterGroupId(int monsterGroupId) {
		this.monsterGroupId = monsterGroupId;
	}

}
