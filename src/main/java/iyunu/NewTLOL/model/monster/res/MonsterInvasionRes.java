public class MonsterInvasionRes {

	private int gangLevel;
	private int groupId;

	/**
	 * @return the gangLevel
	 */
	public int getGangLevel() {
		return gangLevel;
	}

	/**
	 * @param gangLevel
	 *            the gangLevel to set
	 */
	public void setGangLevel(int gangLevel) {
		this.gangLevel = gangLevel;
	}

	/**
	 * @return the groupId
	 */
	public int getGroupId() {
		return groupId;
	}

	/**
	 * @param groupId
	 *            the groupId to set
	 */
	public void setGroupId(int groupId) {
		this.groupId = groupId;
	}

}
