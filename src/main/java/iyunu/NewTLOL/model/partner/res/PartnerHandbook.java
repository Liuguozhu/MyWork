package iyunu.NewTLOL.model.partner.res;

public class PartnerHandbook {

	private int id;
	private int steps;
	private int petId;

	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}

	/**
	 * @param id
	 *            the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}

	/**
	 * @return the steps
	 */
	public int getSteps() {
		return steps;
	}

	/**
	 * @param steps
	 *            the steps to set
	 */
	public void setSteps(int steps) {
		this.steps = steps;
	}

	/**
	 * @return the petId
	 */
	public int getPetId() {
		return petId;
	}

	/**
	 * @param petId
	 *            the petId to set
	 */
	public void setPetId(int petId) {
		this.petId = petId;
	}

}
