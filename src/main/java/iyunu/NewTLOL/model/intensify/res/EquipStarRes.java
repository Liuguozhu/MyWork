package iyunu.NewTLOL.model.intensify.res;

import iyunu.NewTLOL.enumeration.EColor;

public class EquipStarRes {

	private int index;
	private int steps;
	private EColor color;
	private int num;
	private int gold;
	private int rate;
	private int star;
	private int sum;
	private int stone;
	private int propertyPercent; // 加成比例

	/**
	 * @return the index
	 */
	public int getIndex() {
		return index;
	}

	/**
	 * @param index
	 *            the index to set
	 */
	public void setIndex(int index) {
		this.index = index;
	}

	/**
	 * @return the steps
	 */
	public int getSteps() {
		return steps;
	}

	/**
	 * @param steps
	 *            the steps to set
	 */
	public void setSteps(int steps) {
		this.steps = steps;
	}

	/**
	 * @return the color
	 */
	public EColor getColor() {
		return color;
	}

	/**
	 * @param color
	 *            the color to set
	 */
	public void setColor(EColor color) {
		this.color = color;
	}

	/**
	 * @return the num
	 */
	public int getNum() {
		return num;
	}

	/**
	 * @param num
	 *            the num to set
	 */
	public void setNum(int num) {
		this.num = num;
	}

	/**
	 * @return the gold
	 */
	public int getGold() {
		return gold;
	}

	/**
	 * @param gold
	 *            the gold to set
	 */
	public void setGold(int gold) {
		this.gold = gold;
	}

	/**
	 * @return the rate
	 */
	public int getRate() {
		return rate;
	}

	/**
	 * @param rate
	 *            the rate to set
	 */
	public void setRate(int rate) {
		this.rate = rate;
	}

	/**
	 * @return the star
	 */
	public int getStar() {
		return star;
	}

	/**
	 * @param star
	 *            the star to set
	 */
	public void setStar(int star) {
		this.star = star;
	}

	/**
	 * @return the sum
	 */
	public int getSum() {
		return sum;
	}

	/**
	 * @param sum
	 *            the sum to set
	 */
	public void setSum(int sum) {
		this.sum = sum;
	}

	/**
	 * @return the stone
	 */
	public int getStone() {
		return stone;
	}

	/**
	 * @param stone
	 *            the stone to set
	 */
	public void setStone(int stone) {
		this.stone = stone;
	}

	/**
	 * @return the propertyPercent
	 */
	public int getPropertyPercent() {
		return propertyPercent;
	}

	/**
	 * @param propertyPercent
	 *            the propertyPercent to set
	 */
	public void setPropertyPercent(int propertyPercent) {
		this.propertyPercent = propertyPercent;
	}

}
